def seed_content_if_empty(conn: sqlite3.Connection) -> None:
    """Заполняет демо-каталог при пустых таблицах; отзывы и FAQ — если их таблицы пустые."""
    n_cat = conn.execute("SELECT COUNT(*) FROM categories").fetchone()[0]
    if n_cat == 0:
        conn.executemany(
            "INSERT INTO categories (id, name, description, image) VALUES (?, ?, ?, ?)",
            DEMO_CATEGORIES,
        )
    n_prod = conn.execute("SELECT COUNT(*) FROM products").fetchone()[0]
    if n_prod == 0:
        conn.executemany(
            """INSERT INTO products
            (id, name, description, price, category, images, materials, size, technique, in_stock, featured, created_at)
            VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?)""",
            DEMO_PRODUCTS,
        )

    n_rev = conn.execute("SELECT COUNT(*) FROM reviews").fetchone()[0]
    if n_rev == 0:
        conn.executemany(
            "INSERT INTO reviews (id, author, rating, text, date, product_id) VALUES (?, ?, ?, ?, ?, ?)",
            DEMO_REVIEWS,
        )

    n_faq = conn.execute("SELECT COUNT(*) FROM faqs").fetchone()[0]
    if n_faq == 0:
        conn.executemany(
            "INSERT INTO faqs (id, question, answer, category) VALUES (?, ?, ?, ?)",
            DEMO_FAQS,
        )
